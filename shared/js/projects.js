function displayProjects(parent, lang = "en") {
	const topProjectsDiv = document.createElement("div");
	topProjectsDiv.id = "top_projects";
	topProjectsDiv.className = "projects_list";
	parent.appendChild(topProjectsDiv);

	const topProjectsTitle = document.createElement("h1");
	topProjectsTitle.innerHTML = (lang == "en" ? "Top projects" : "Projets mis en avant");
	topProjectsDiv.appendChild(topProjectsTitle);
	const topProjectsContainer = document.createElement("div");
	topProjectsDiv.appendChild(topProjectsContainer);

	const allProjectsDiv = document.createElement("div");
	allProjectsDiv.id = "all_projects";
	allProjectsDiv.className = "projects_list";
	parent.appendChild(allProjectsDiv);

	const allProjectsTitle = document.createElement("h1");
	allProjectsTitle.innerHTML = (lang == "en" ? "All projects" : "Tous les projets");
	allProjectsDiv.appendChild(allProjectsTitle);
	const allProjectsContainer = document.createElement("div");
	allProjectsDiv.appendChild(allProjectsContainer);

	for (let i = 0; i < PROJECTS.length; i++) {
		const project = PROJECTS[i];
		const title = (lang == "en" ? project.title_en : project.title_fr);

		const projectLink = document.createElement("a");
		projectLink.href = "../projects/" + project.link + "/index.html";
		allProjectsContainer.appendChild(projectLink);

		if (project.img_path == "") {
			const projectPreview = document.createElement("div");
			projectPreview.innerHTML = title;
			projectLink.appendChild(projectPreview);
		} else {
			const projectImage = document.createElement("img");
			const imgSrc = "../projects/" + project.link + "/";
			projectImage.width = 320;
			projectImage.height = 180;
			projectImage.src = imgSrc + ("preview.webp");
			projectImage.alt = title + " background image";
			projectLink.appendChild(projectImage);
		}

		const projectDescDiv = document.createElement("div");
		projectDescDiv.innerHTML = title;
		projectLink.appendChild(projectDescDiv);

		if (project.top == true) {
			const clone = projectLink.cloneNode(true);
			topProjectsContainer.appendChild(clone);
		}
	}

	const randomLink = document.createElement("a");
	const project = PROJECTS[random_int(0, PROJECTS.length - 1)];
	randomLink.href = "../projects/" + project.link + "/index.html";
	const randTitle = (lang == "en" ? "Random project" : "Projet au hasard")
	const randomLinkDesc = document.createElement("div");
	randomLinkDesc.innerHTML = randTitle;
	randomLink.appendChild(randomLinkDesc);
	randomLink.appendChild(randomLinkDesc.cloneNode(true));
	topProjectsContainer.appendChild(randomLink);
}

